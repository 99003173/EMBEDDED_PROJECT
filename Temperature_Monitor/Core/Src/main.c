void SETUP(void)
{
	OUTPUT_PIN (DHT11_GPIO_Port, DHT11_Pin);
	HAL_GPIO_WritePin (DHT11_GPIO_Port, DHT11_Pin, DISABLE);
	delay(18000);
	HAL_GPIO_WritePin(DHT11_GPIO_Port, DHT11_Pin, ENABLE);
	delay(20);
	INPUT_PIN(DHT11_GPIO_Port, DHT11_Pin);
}

uint8_t INPUT_CHECK(void)
{
	uint8_t FLAG = 0;
	delay (40);
	if (!(HAL_GPIO_ReadPin (DHT11_GPIO_Port, DHT11_Pin)))
	{
		delay (80);
		if ((HAL_GPIO_ReadPin (DHT11_GPIO_Port, DHT11_Pin)))
			FLAG = 1;
		else
			FLAG = -1;
	}
	while ((HAL_GPIO_ReadPin (DHT11_GPIO_Port, DHT11_Pin)));
	return FLAG;
}

uint8_t READ_SENSOR(void)
{
	uint8_t i,j;
	for (j=0;j<8;j++)
	{
		while (!(HAL_GPIO_ReadPin (DHT11_GPIO_Port, DHT11_Pin)));
		delay (40);
		if (!(HAL_GPIO_ReadPin (DHT11_GPIO_Port, DHT11_Pin)))
		{
			i&= ~(1<<(7-j));
		}
		else
		{
			i|= (1<<(7-j));
		}
		while ((HAL_GPIO_ReadPin (DHT11_GPIO_Port, DHT11_Pin)));
	}
	return i;
}

