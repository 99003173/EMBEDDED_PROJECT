int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_TIM6_Init();
  MX_UART4_Init();
  HAL_TIM_Base_Start(&htim6);

  while (1)
  {
      HAL_GPIO_WritePin(SS_GPIO_Port, SS_Pin, DISABLE);
      HAL_GPIO_TogglePin(ORANGE_GPIO_Port, ORANGE_Pin);
      SETUP();
      FLAG = INPUT_CHECK();
      if (FLAG == 1)
      {
          HUM_HIGH = READ_SENSOR();
          HUM_LOW = READ_SENSOR();
          TEMP_HIGH = READ_SENSOR();
          TEMP_LOW = READ_SENSOR();
          PARITY_CHECK = READ_SENSOR();
          HUM_FULL = HUM_HIGH;
          TEMP_FULL = TEMP_HIGH;
          REAL_TEMP = (int)TEMP_FULL;
          REAL_HUM = (int)HUM_FULL;
          uint8_t H = (uint8_t)REAL_HUM;
          uint8_t T = (uint8_t)REAL_TEMP;
          if(T>15 && T<=23)                                         //Ambient Temperature Condition
          {
              HAL_GPIO_TogglePin(GREEN_GPIO_Port, GREEN_Pin);
          }
          if(T>23)
          {
              HAL_GPIO_TogglePin(RED_GPIO_Port, RED_Pin);
          }
          HAL_SPI_Transmit(&hspi1, &T, 1, 10);
          HAL_Delay(100);
      }
      delay(1000);
  }
  return 0;
}


